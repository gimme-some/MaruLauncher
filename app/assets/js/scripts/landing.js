// Robust Minecraft Server List Ping. Replaces helios-core getServerStatus(),
// which caps reads at 5 TCP iterations (maxTries) and so falsely reports a
// server with a favicon (large status response split across many packets) as
// OFFLINE. This reads until the full packet arrives, with a generous timeout.
function mcWriteVarInt(value){
    const bytes = []
    let v = value >>> 0
    do {
        let temp = v & 0x7f
        v >>>= 7
        if(v !== 0) temp |= 0x80
        bytes.push(temp)
    } while(v !== 0)
    return Buffer.from(bytes)
}
function mcReadVarInt(buf, offset){
    let numRead = 0, result = 0, read
    do {
        if(offset + numRead >= buf.length) return null // incomplete, need more bytes
        read = buf[offset + numRead]
        result |= (read & 0x7f) << (7 * numRead)
        numRead++
        if(numRead > 5) throw new Error('VarInt too big')
    } while((read & 0x80) !== 0)
    return { value: result >>> 0, size: numRead }
}
function mcPacket(dataBuf){
    return Buffer.concat([mcWriteVarInt(dataBuf.length), dataBuf])
}
async function pingServerStatus(hostname, port = 25565, protocol = 763, timeoutMs = 10000){
    // Resolve SRV record if present; tolerate failure and use host/port as-is.
    try {
        const recs = await dnsPromises.resolveSrv(`_minecraft._tcp.${hostname}`)
        if(recs && recs.length > 0){
            hostname = recs[0].name
            port = recs[0].port
        }
    } catch(_){ /* no SRV record */ }

    return await new Promise((resolve, reject) => {
        let settled = false
        const finish = (ok, payload) => {
            if(settled) return
            settled = true
            try { socket.destroy() } catch(_){ /* already closed */ }
            ok ? resolve(payload) : reject(payload)
        }
        const socket = net.connect(port, hostname, () => {
            const host = Buffer.from(hostname, 'utf8')
            const portBuf = Buffer.alloc(2)
            portBuf.writeUInt16BE(port, 0)
            // Handshake (next state = status) then status request.
            socket.write(mcPacket(Buffer.concat([
                mcWriteVarInt(0x00), mcWriteVarInt(protocol),
                mcWriteVarInt(host.length), host, portBuf, mcWriteVarInt(1)
            ])))
            socket.write(mcPacket(mcWriteVarInt(0x00)))
        })
        socket.setTimeout(timeoutMs, () => finish(false, new Error('Server status timed out')))
        let buffer = Buffer.alloc(0)
        socket.on('data', (chunk) => {
            buffer = Buffer.concat([buffer, chunk])
            const lenVar = mcReadVarInt(buffer, 0)
            if(lenVar == null) return
            if(buffer.length < lenVar.size + lenVar.value) return // wait for the whole packet
            let off = lenVar.size
            const typeVar = mcReadVarInt(buffer, off)
            if(typeVar == null) return
            off += typeVar.size
            if(typeVar.value !== 0x00) return finish(false, new Error('Unexpected status packet type'))
            const strVar = mcReadVarInt(buffer, off)
            if(strVar == null) return
            off += strVar.size
            if(buffer.length < off + strVar.value) return // wait for the full JSON string
            try {
                finish(true, JSON.parse(buffer.toString('utf8', off, off + strVar.value)))
            } catch(_){
                finish(false, new Error('Failed to parse server status JSON'))
            }
        })
        socket.on('error', (err) => finish(false, err))
        socket.on('end', () => finish(false, new Error('Connection closed before status received')))
    })
}

const refreshServerStatus = async (fade = false) => {
    loggerLanding.info('Refreshing Server Status')
    const serv = (await DistroAPI.getDistribution()).getServerById(ConfigManager.getSelectedServer())

    let pLabel = Lang.queryJS('landing.serverStatus.server')
    let pVal = Lang.queryJS('landing.serverStatus.offline')
    let online = false

    // pingServerStatus() is our own robust ping (no 5-iteration read cap, 10s
    // timeout). Retry once to ride out a transient network blip before declaring
    // the server offline.
    const pingWithRetry = async (tries = 2, delayMs = 1500) => {
        let lastErr
        for(let i = 0; i < tries; i++){
            try {
                return await pingServerStatus(serv.hostname, serv.port, 763)
            } catch (err) {
                lastErr = err
                if(i < tries - 1){
                    await new Promise(r => setTimeout(r, delayMs))
                }
            }
        }
        throw lastErr
    }

    try {

        const servStat = await pingWithRetry()
        pLabel = Lang.queryJS('landing.serverStatus.players')
        pVal = (servStat.players?.online ?? 0) + '/' + (servStat.players?.max ?? 0)
        online = true

    } catch (err) {
        loggerLanding.warn('Unable to refresh server status, assuming offline.')
        loggerLanding.debug(err)
    }
    // Drives the status dot color in the dock (CSS [status] attribute hook).
    document.getElementById('server_status_wrapper').setAttribute('status', online ? 'online' : 'offline')
    if(fade){
        $('#server_status_wrapper').fadeOut(250, () => {
            document.getElementById('landingPlayerLabel').innerHTML = pLabel
            document.getElementById('player_count').innerHTML = pVal
            $('#server_status_wrapper').fadeIn(500)
        })
    } else {
        document.getElementById('landingPlayerLabel').innerHTML = pLabel
        document.getElementById('player_count').innerHTML = pVal
    }
    
}

